// PIA-style I/O hooks. Strings here are eval'd inside handleBusRead /
// handleBusWrite. For reads, the expression value is returned as the
// byte the chip sees on the data bus. For writes, the variable `d`
// holds the byte the chip is asserting.
writeTriggers[0xD012] = "if (typeof appendApple1Char === 'function') { appendApple1Char(d & 0x7F); }";
readTriggers[0xD010]  = "var c = (typeof appleKeyQueue !== 'undefined' && appleKeyQueue.length) ? appleKeyQueue.shift() : 0; (c | 0x80)";
readTriggers[0xD011]  = "((typeof appleKeyQueue !== 'undefined' && appleKeyQueue.length) ? 0xA7 : 0x00)";
// Reads of DSP must always show the display-ready bit (B7) clear or
// WozMon's ECHO loop (BIT DSP / BMI ECHO) would spin forever after the
// first character: the simulated memory holds whatever was last written
// to $D012, which has bit 7 set on every printable character.
readTriggers[0xD012]  = "0";
